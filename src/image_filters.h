#include "image_tools.h"
/**
 * @brief Преобразует изображение в оттенки серого
 * @param img Матрица изображения
 * @note Использует стандартные коэффициенты яркости:
 *       R: 0.2126, G: 0.7152, B: 0.0722
 */
void grayscale(matrix& img);
/**
 * @brief Применяет гауссово размытие к изображению
 * @param img Матрица изображения
 * @param sigma Стандартное отклонение гауссова ядра (радиус размытия)
 * @note Рекомендуемые значения sigma: 0.5 (легкое) - 5.0 (сильное)
 */
void gaussian_blur(matrix& img, float sigma);